package package1;

public class SimpleBean {
    private String name;
    private int age;
    public SimpleBean(){

    }
    public void setName(String name){
        this.name=name;
    }
    public void setAge(int age){
        this.age=age;
    }
    public String getName(){
        return this.name;
    }
    public int getAge(){
        return this.age;
    }
}
